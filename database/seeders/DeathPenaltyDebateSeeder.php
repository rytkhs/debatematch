問題3の2: 死刑の運用は恣意的にならざるを得ず、不平等な刑罰です。
明治大学の菊田教授は、『いま、なぜ死刑廃止か』という著書において、次のように述べています。
（引用開始）
「三井明・元裁判官は、自らの経験として、『これはこの裁判官がやったから死刑になったが、他の裁判官がやったら死刑にならなかったのではないか』という死刑判決基準のばらつきがあったことを報告している。」
（引用終わり）
死刑と無期懲役の間には、生命を維持できるか否かという極めて大きな隔たりがあります。にもかかわらず、この二つの量刑を分ける明確な基準が存在せず、被告人の弁護能力や裁判官の心証によって左右される現状は、著しく不合理と言わざるを得ません。
以上の2点、すなわち誤判の場合に取り返しがつかないこと、そして無期懲役刑との間で恣意的な運用が避けられないことから、死刑は刑罰として危険性と不平等性という根本的な欠陥を有すると主張します。

次に、我々のプランを採用することによって得られるメリットを整理して述べます。
第一に、前述した3つの大きな問題点がすべて解決されます。
第二に、それに加えて、以下の3つの付加的なメリットが得られます。

追加メリット1: 死刑を廃止することにより、死刑執行という非人間的な職務から刑務官を解放することができます。

追加メリット2: また、死刑廃止は、死刑執行によって生じる死刑囚の家族や友人の新たな悲しみを防ぐことができます。

追加メリット3: さらに、死刑を廃止することで、刑事司法制度が負担している膨大なコストを削減できます。
死刑事件は、その審理に慎重を期すため、膨大な労力、時間、費用が費やされるのが実情です。これにより、裁判全体の迅速性や確実性が損なわれるという問題が生じています。これは見過ごされがちな点ですが、死刑制度がもたらす大きな問題の一つであり、死刑廃止によって解消できます。
MESSAGE;

        $this->createMessage($debate, $debate->affirmative_user_id, $message, 1, $baseTime, $messageIndex);
