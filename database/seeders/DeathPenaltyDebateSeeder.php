        // Turn 7: 否定側第二反駁
        $this->addNegativeSecondRebuttal($debate, $baseTime, $messageIndex);

        // Turn 8: 肯定側第二反駁
        $this->addAffirmativeSecondRebuttal($debate, $baseTime, $messageIndex);
    }

