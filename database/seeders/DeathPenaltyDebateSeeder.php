弊害: 犯罪増加

A. 人間は本能的に生命を欲しているため、死刑には強い犯罪抑止効果があります。
朝日大学法学部教授の三原憲三氏は、1990年に次のように述べています。
（引用開始）
「（小野清一郎博士の主張によると）人間は本能的に生を欲する。『一人の生命は全地球より重い』というのも、個人の生命欲のやるせなさから出てくる。そうである限り、死刑の存在は罪を犯そうとする者にとっては、大きな心理的抑制力をもつ。もちろん死刑を廃止したら翌日から凶悪な犯罪が続出するなどというものではない。長期にわたって、死刑の存在が人間の本能そのものを抑制する。社会的心理的なコンプレックスを形成するのである。」
（引用終わり）

B. 死刑廃止によって犯罪が増加します。
筑波大学教授の小田晋氏は、1994年に次のように述べています。
（引用開始）
「団藤重光氏が『死刑廃止論』で、『死刑に犯罪抑制力はない』と述べていることから、一見これは定説となっているように見えます。しかし、死刑の抑制力（デテレント・エフェクト）についての文献をみれば、必ずしもそうはいえないことがわかります。というのも、現在では、せいぜい死刑の犯罪抑制力について統計的に証明できるか、という問題をめぐって賛否の議論があるだけだからです。たとえば、アメリカの犯罪研究者であるS・スタックは、多くの社会科学者が主張する、死刑に犯罪抑制効果はないという考えに対立して、死刑執行が公表された場合、それは強い殺人抑制力を持つと論じました（一九八七年）。彼によると、広く公表された死刑執行は、一九五〇年～八〇年の間に少なくとも四八〇人の無辜の人命を救ったとされています。」
（引用終わり）

続いて、肯定側の議論に対する反論に移ります。

まず、肯定側は「死刑は国家による殺人である」と主張しましたが、最高裁判決において死刑は合憲と判断されています。
中央学院大学教授の重松一義氏は、1995年に次のように述べています。
（引用開始）
「『憲法13条においては、すべての国民は個人として最大の尊重を必要とする旨を規定している。しかし同時に、同条に於いては、公共の福祉という基本的原則に反する場合には、生命に関する国民の権利といえども、立法上制限乃至剥奪せられることを当然予想しているものと云わねばならぬ』、『死刑は窮極の刑罰であり、また冷厳な刑罰ではあるが、刑罰としての死刑そのものは、憲法36条のいわゆる残虐な刑罰にあたらない』」
（引用終わり）
したがって、死刑は国家による殺人とは言えません。

次に、肯定側は「死刑に犯罪抑制力はない」と主張しましたが、これは先ほど弊害として指摘した議論を適用してください。

さらに、肯定側は「死刑は贖罪の気持ちをなくす」と論じましたが、むしろ贖罪の気持ちは死刑があるからこそ生まれるものです。死刑の存在が、犯罪者に贖罪の気持ちを抱かせるのです。
したがって、これは反転の議論となりますが、肯定側のプランである死刑廃止によって、死刑囚の贖罪の気持ちが失われる可能性があります。
産経新聞論説委員の飯田政之氏は、1993年に次のように述べています。
（引用開始）
「法務省幹部からの電話は『最近死刑の執行がなく、外部から死刑がなくなるらしい、との情報を得た死刑囚から"自分たちは助かるらしいが…"と質問を受け刑務官が答えに窮している』との指摘である。心からの反省と自分が手にかけた被害者への贖罪の気持ちから明鏡止水の境地に達していた死刑囚も、生への気持ちがわいたとたん心の落ちつきを失い、贖罪の気持ちが薄れたようだ、との見解だ。」
（引用終わり）

また、肯定側は「誤判の場合、死刑は取り返しがつかない」と主張しましたが、現在の日本では誤判の可能性はほとんどゼロです。
帝京大学教授の藤永克己氏は、1996年に次のように述べています。
（引用開始）
「しかし、わが国の死刑制度の運用は、世界の存置国の中で最も明確な基準を有し、公正で慎重になされている。凶悪犯が増加しつつあるとの印象を免れない最近の犯罪情勢の中でも、死刑が言い渡される率は、通常の殺人罪では0.25パーセント (400人の殺人犯のうち1人だけ)、強盗殺人犯でも10パーセント(100人の強盗殺人犯のうち10人だけ)にとどまるなど、数の上だけでなく、事実を解明する証拠調べ手続においても、極めて慎重な審理がなされている。現在、誤判の可能性は限りなくゼロに近いといってよい。」
（引用終わり）
さらに、経験的に、日本では死刑に関して誤判は起きていません。
弁護士の島田荘司氏は、1993年に次のように述べています。
（引用開始）
「私は一審で死刑、二審で無罪になった被告人の弁護をしたことがあります。一審で死刑、二審で無期懲役になった人の弁護をしたこともありますが、死刑執行を受けた者で、誤判であったことが分かった例を私は未だかつて日本では聞かないのであります。」
（引用終わり）
したがって、これも反転の議論となりますが、死刑の存在が誤判を防いでいると言えます。
